# ---------------------------------------------------------------------------
# session_range - high/low of bars inside an ET window.
# ---------------------------------------------------------------------------
def test_session_range_window_high_low():
    # 30-min bars across a full UTC day; inject a clear high/low ONLY inside
    # 10:00-11:00 ET (the 10:00 and 10:30 ET bars).
    idx = pd.date_range(start="2024-03-04 00:00", periods=48, freq="30min", tz="UTC")
    rows = []
    for ts in idx:
        et = ts.tz_convert(ET)
        if et.hour == 10 and et.minute == 0:
            rows.append([100, 110, 99, 105, 1])     # window HIGH = 110
        elif et.hour == 10 and et.minute == 30:
            rows.append([105, 106, 90, 95, 1])        # window LOW = 90
        else:
            rows.append([100, 101, 99, 100, 1])
    df = pd.DataFrame(rows, index=idx, columns=["open", "high", "low", "close", "volume"])

    hi, lo = session_range(df, "10:00", "11:00")
    assert hi == 110.0
    assert lo == 90.0

    # A window with NO bars (between the :00 and :30 marks) -> (None, None).
    assert session_range(df, "10:05", "10:25") == (None, None)


def test_session_range_accepts_time_tuples():
    idx = pd.date_range(start="2024-03-04 00:00", periods=48, freq="30min", tz="UTC")
    rows = []
    for ts in idx:
        et = ts.tz_convert(ET)
        rows.append([100, 120 if et.hour == 14 else 101, 80 if et.hour == 14 else 99, 100, 1])
    df = pd.DataFrame(rows, index=idx, columns=["open", "high", "low", "close", "volume"])
    hi, lo = session_range(df, (14, 0), (15, 0))
    assert hi == 120.0 and lo == 80.0


def test_session_range_empty_frame():
    assert session_range(pd.DataFrame(), "10:00", "11:00") == (None, None)


