def test_price_in_fvg():
    fvg = FairValueGap(direction="bullish", high=110.0, low=100.0, midpoint=105.0,
                       bar_index=5, timestamp=pd.Timestamp("2024-01-01"), size_ticks=40,
                       ce_level=105.0)
    assert price_in_fvg(105.0, fvg) is True
    assert price_in_fvg(100.0, fvg) is True   # inclusive edge
    assert price_in_fvg(111.0, fvg) is False
    assert price_in_fvg(100.0, fvg, inclusive=False) is False
    assert price_in_fvg(100.0, None) is False


# ---------------------------------------------------------------------------
# New killzone session keys via the (extended) is_in_session.
# ---------------------------------------------------------------------------
def _at_et(hour, minute=0):
    """A tz-aware UTC timestamp that maps to the given ET wall-clock.

    Build the instant in America/New_York (handles day rollover for late/early
    hours) then convert to UTC, so the engine's is_in_session reproduces the
    intended ET hour regardless of DST.
    """
    naive = pd.Timestamp(f"2024-03-04 {hour:02d}:{minute:02d}")
    return naive.tz_localize(ET).tz_convert("UTC")


@pytest.mark.parametrize("key,inside,outside", [
    ("SILVER_BULLET", (10, 30), (9, 30)),
    ("NY_OPEN", (9, 45), (10, 15)),
    ("NY_PM_ICT", (14, 0), (13, 0)),
    ("LONDON_OPEN", (2, 30), (3, 30)),
])
def test_new_session_keys(key, inside, outside):
    assert is_in_session(_at_et(*inside), [key]) is True
    assert is_in_session(_at_et(*outside), [key]) is False


def test_existing_session_keys_unchanged():
    # Regression: the original keys still behave as before.
    assert is_in_session(_at_et(10, 0), ["NY_AM"]) is True      # 10:00 in 09:30-12:00
    assert is_in_session(_at_et(14, 30), ["NY_PM"]) is True     # 14:30 in 14:00-15:00
    assert is_in_session(_at_et(13, 0), ["NY_PM"]) is False
    assert is_in_session(_at_et(3, 0), ["LONDON"]) is True      # 03:00 in 02:00-05:00
    assert is_in_session(_at_et(21, 0), ["ASIA"]) is True       # 21:00 in 20:00-24:00
