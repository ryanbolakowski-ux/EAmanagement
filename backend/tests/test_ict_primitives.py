# ---------------------------------------------------------------------------
# detect_mss - reversal up.
# ---------------------------------------------------------------------------
def test_detect_mss_reversal_up():
    df = _df([list(r) for r in _DOWN_ROWS])
    mss = detect_mss(df, lookback=3)
    assert isinstance(mss, MSS), f"expected an MSS, got {mss!r}"
    assert mss.direction == "up", f"expected bullish reversal, got {mss.direction}"
    assert mss.prior_trend == "down"
    assert mss.bar_index == len(df) - 1
    # Broke the most-recent counter-trend swing HIGH (~93.5).
    assert mss.broken_level == pytest.approx(93.5)


def test_detect_mss_none_on_continuation():
    """A downtrend that keeps drifting down (no reclaim) -> no MSS."""
    rows = [list(r) for r in _DOWN_ROWS]
    rows[-1] = [84.0, 84.3, 82.0, 83.0, 100]  # small bearish bar, no reclaim
    assert detect_mss(_df(rows), lookback=3) is None


def test_detect_mss_requires_displacement():
    """Reclaim above the swing high but WITHOUT displacement -> no MSS.

    Close just above 93.5 on a tiny-range bar (fails the body/range gate)."""
    rows = [list(r) for r in _DOWN_ROWS]
    rows[-1] = [93.6, 93.8, 93.5, 93.7, 100]  # above 93.5 but tiny range
    assert detect_mss(_df(rows), lookback=3) is None


