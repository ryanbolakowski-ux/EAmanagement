# ote_levels - 0.705 focal level lands at the right price.
# ---------------------------------------------------------------------------
def test_ote_levels_bullish():
    # Impulse low->high 100 -> 200 (leg = 100). Long retraces DOWN from 200.
    lv = ote_levels(swing_high=200.0, swing_low=100.0, direction="long")
    assert lv[0.62] == pytest.approx(138.0)
    assert lv[0.705] == pytest.approx(129.5)
    assert lv[0.79] == pytest.approx(121.0)
    assert lv["entry"] == pytest.approx(129.5)
    assert lv["direction"] == "long"


def test_ote_levels_bearish():
    lv = ote_levels(swing_high=200.0, swing_low=100.0, direction="short")
    assert lv[0.705] == pytest.approx(170.5)
    assert lv["entry"] == pytest.approx(170.5)
    assert lv["direction"] == "short"


